package com.example.application.task_15;

public class DataStructure {
    private String mUrl;
    private String mTitle;

    public DataStructure(String url, String title) {
        mUrl = url;
        mTitle = title;
    }

    public String getUrl() {
        return mUrl;
    }

    public String getTitle() {
        return mTitle;
    }
}
